SELECT
'Product' as 'Item Type',
      CASE
            WHEN IM_ITEM.TRK_METH = 'G' OR IM_ITEM.TRK_METH = 'N'
            THEN REPLACE(REPLACE(IM_ITEM.TRK_METH,'G','SKU'),'N','PRODUCT')
      END as 'Item Type',
       IM_ITEM.ITEM_NO
       --IM_INV_CELL.QTY_AVAIL
FROM IM_ITEM

JOIN IM_BARCOD
ON IM_ITEM.ITEM_NO = IM_BARCOD.ITEM_NO

JOIN IM_INV
ON IM_ITEM.ITEM_NO = IM_INV.ITEM_NO



WHERE IM_INV.LOC_ID = 'MAIN' AND
      IM_BARCOD.BARCOD_ID = 'ITEM'
